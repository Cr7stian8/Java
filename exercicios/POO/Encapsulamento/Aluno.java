package Encapsulamento;

public class Aluno {
	//Criando atributos
	private String nome;
	private String idade;
	
	//Criando getters and setters
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getIdade() {
		return idade;
	}
	public void setIdade(String idade) {
		this.idade = idade;
	}
	
}